def find_column(dataframe, possible_names, description):

    for column_name in possible_names:

        if column_name in dataframe.columns:

            return column_name


    raise ValueError(

        f"\nCould not detect {description} column.\n\n"

        f"Expected one of:\n"

        f"{possible_names}\n\n"

        f"Available columns:\n"

        f"{list(dataframe.columns)}"

    )


def normalize_state(value):

    if pd.isna(value):

        raise ValueError(
            "Missing assembly state encountered."
        )


    # ------------------------------------------------------------
    # NUMERIC STATE
    # ------------------------------------------------------------

    if isinstance(
        value,
        (
            int,
            np.integer,
            float,
            np.floating,
        ),
    ):

        numeric_value = int(value)

        if numeric_value in ID_TO_STATE:

            return ID_TO_STATE[numeric_value]


    # ------------------------------------------------------------
    # STRING STATE
    # ------------------------------------------------------------

    state = str(value).strip().upper()


    aliases = {

        "EMPTY": "EMPTY",

        "MARKER_ONLY": "MARKER_ONLY",

        "MARKER ONLY": "MARKER_ONLY",

        "POWER_ADAPTER_ONLY": "POWER_ADAPTER_ONLY",

        "POWER ADAPTER ONLY": "POWER_ADAPTER_ONLY",

        "ADAPTER_ONLY": "POWER_ADAPTER_ONLY",

        "ADAPTER ONLY": "POWER_ADAPTER_ONLY",

        "BOTH_PRESENT": "BOTH_PRESENT",

        "BOTH PRESENT": "BOTH_PRESENT",

        "BOTH": "BOTH_PRESENT",

        "0": "EMPTY",

        "1": "MARKER_ONLY",

        "2": "POWER_ADAPTER_ONLY",

        "3": "BOTH_PRESENT",
    }


    if state not in aliases:

        raise ValueError(

            f"Unknown assembly state encountered: {value}"

        )


    return aliases[state]


def safe_divide(numerator, denominator):

    if denominator == 0:

        return 0.0

    return numerator / denominator


def calculate_binary_metrics(
    ground_truth,
    predictions,
):

    ground_truth = np.asarray(
        ground_truth,
        dtype=int,
    )


    predictions = np.asarray(
        predictions,
        dtype=int,
    )


    true_positive = int(

        np.sum(

            (ground_truth == 1)

            &

            (predictions == 1)

        )

    )


    true_negative = int(

        np.sum(

            (ground_truth == 0)

            &

            (predictions == 0)

        )

    )


    false_positive = int(

        np.sum(

            (ground_truth == 0)

            &

            (predictions == 1)

        )

    )


    false_negative = int(

        np.sum(

            (ground_truth == 1)

            &

            (predictions == 0)

        )

    )


    total = len(ground_truth)


    accuracy = safe_divide(

        true_positive + true_negative,

        total,

    )


    precision = safe_divide(

        true_positive,

        true_positive + false_positive,

    )


    recall = safe_divide(

        true_positive,

        true_positive + false_negative,

    )


    f1_score = safe_divide(

        2 * precision * recall,

        precision + recall,

    )


    return {

        "accuracy": accuracy,

        "precision": precision,

        "recall": recall,

        "f1_score": f1_score,

        "true_positive": true_positive,

        "true_negative": true_negative,

        "false_positive": false_positive,

        "false_negative": false_negative,
    }


def calculate_confusion_matrix(
    ground_truth_states,
    predicted_states,
):

    confusion_matrix = np.zeros(

        (

            len(STATE_NAMES),

            len(STATE_NAMES),

        ),

        dtype=int,

    )


    for ground_truth_state, predicted_state in zip(

        ground_truth_states,

        predicted_states,

    ):

        ground_truth_id = STATE_TO_ID[
            ground_truth_state
        ]


        predicted_id = STATE_TO_ID[
            predicted_state
        ]


        confusion_matrix[
            ground_truth_id,
            predicted_id,
        ] += 1


    return confusion_matrix


def calculate_per_state_metrics(
    confusion_matrix,
):

    rows = []


    for state_id, state_name in enumerate(
        STATE_NAMES
    ):

        true_positive = int(

            confusion_matrix[
                state_id,
                state_id,
            ]

        )


        false_positive = int(

            confusion_matrix[
                :,
                state_id,
            ].sum()

            - true_positive

        )


        false_negative = int(

            confusion_matrix[
                state_id,
                :,
            ].sum()

            - true_positive

        )


        support = int(

            confusion_matrix[
                state_id,
                :,
            ].sum()

        )


        precision = safe_divide(

            true_positive,

            true_positive + false_positive,

        )


        recall = safe_divide(

            true_positive,

            true_positive + false_negative,

        )


        f1_score = safe_divide(

            2 * precision * recall,

            precision + recall,

        )


        rows.append(

            {

                "assembly_state": state_name,

                "precision": precision,

                "recall": recall,

                "f1_score": f1_score,

                "support": support,

                "true_positive": true_positive,

                "false_positive": false_positive,

                "false_negative": false_negative,

            }

        )


    return pd.DataFrame(rows)


def extract_error_segments(
    comparison_dataframe,
):

    error_frames = comparison_dataframe[

        comparison_dataframe["state_correct"]
        == 0

    ].copy()


    if error_frames.empty:

        return pd.DataFrame(

            columns=[

                "error_segment_id",

                "start_frame",

                "end_frame",

                "start_time_seconds",

                "end_time_seconds",

                "duration_frames",

                "duration_seconds",

                "ground_truth_states",

                "predicted_states",

            ]

        )


    error_frames = error_frames.sort_values(

        by="frame_number"

    ).reset_index(drop=True)


    error_frames["new_segment"] = (

        error_frames["frame_number"]
        .diff()
        .fillna(1)

        != 1

    ).astype(int)


    error_frames["error_segment_id"] = (

        error_frames["new_segment"].cumsum()

    )


    segment_rows = []


    for segment_id, segment in error_frames.groupby(

        "error_segment_id"

    ):

        start_frame = int(
            segment["frame_number"].iloc[0]
        )


        end_frame = int(
            segment["frame_number"].iloc[-1]
        )


        start_time = float(
            segment["time_seconds"].iloc[0]
        )


        end_time = float(
            segment["time_seconds"].iloc[-1]
        )


        duration_frames = len(segment)


        if duration_frames > 1:

            time_step = float(

                segment["time_seconds"]
                .diff()
                .dropna()
                .median()

            )

        else:

            time_step = 0.0


        duration_seconds = (

            end_time
            - start_time
            + time_step

        )


        ground_truth_states = "|".join(

            sorted(

                segment[
                    "assembly_state_gt"
                ].unique()

            )

        )


        predicted_states = "|".join(

            sorted(

                segment[
                    "assembly_state_pred"
                ].unique()

            )

        )


        segment_rows.append(

            {

                "error_segment_id":
                    int(segment_id),

                "start_frame":
                    start_frame,

                "end_frame":
                    end_frame,

                "start_time_seconds":
                    start_time,

                "end_time_seconds":
                    end_time,

                "duration_frames":
                    duration_frames,

                "duration_seconds":
                    duration_seconds,

                "ground_truth_states":
                    ground_truth_states,

                "predicted_states":
                    predicted_states,

            }

        )


    return pd.DataFrame(segment_rows)


def extract_transitions(
    dataframe,
    frame_column,
    time_column,
    state_column,
):

    transitions = []


    previous_state = None


    for _, row in dataframe.iterrows():

        current_state = row[state_column]


        if previous_state is None:

            previous_state = current_state

            continue


        if current_state != previous_state:

            transitions.append(

                {

                    "frame_number":
                        int(row[frame_column]),

                    "time_seconds":
                        float(row[time_column]),

                    "from_state":
                        previous_state,

                    "to_state":
                        current_state,

                }

            )


            previous_state = current_state


    return pd.DataFrame(transitions)


def compare_transitions(
    ground_truth_transitions,
    predicted_transitions,
):

    output_rows = []


    used_prediction_indices = set()


    for ground_truth_index, ground_truth_row in (
        ground_truth_transitions.iterrows()
    ):

        matching_candidates = []


        for prediction_index, prediction_row in (
            predicted_transitions.iterrows()
        ):

            if prediction_index in used_prediction_indices:

                continue


            same_transition = (

                prediction_row["from_state"]
                == ground_truth_row["from_state"]

                and

                prediction_row["to_state"]
                == ground_truth_row["to_state"]

            )


            if same_transition:

                frame_error = (

                    int(
                        prediction_row[
                            "frame_number"
                        ]
                    )

                    -

                    int(
                        ground_truth_row[
                            "frame_number"
                        ]
                    )

                )


                matching_candidates.append(

                    (

                        abs(frame_error),

                        frame_error,

                        prediction_index,

                        prediction_row,

                    )

                )


        if matching_candidates:

            matching_candidates.sort(

                key=lambda item: item[0]

            )


            (

                absolute_frame_error,

                signed_frame_error,

                selected_prediction_index,

                selected_prediction_row,

            ) = matching_candidates[0]


            used_prediction_indices.add(

                selected_prediction_index

            )


            time_error = (

                float(
                    selected_prediction_row[
                        "time_seconds"
                    ]
                )

                -

                float(
                    ground_truth_row[
                        "time_seconds"
                    ]
                )

            )


            output_rows.append(

                {

                    "ground_truth_transition_id":
                        ground_truth_index + 1,

                    "from_state":
                        ground_truth_row["from_state"],

                    "to_state":
                        ground_truth_row["to_state"],

                    "ground_truth_frame":
                        int(
                            ground_truth_row[
                                "frame_number"
                            ]
                        ),

                    "predicted_frame":
                        int(
                            selected_prediction_row[
                                "frame_number"
                            ]
                        ),

                    "signed_frame_error":
                        signed_frame_error,

                    "absolute_frame_error":
                        absolute_frame_error,

                    "ground_truth_time_seconds":
                        float(
                            ground_truth_row[
                                "time_seconds"
                            ]
                        ),

                    "predicted_time_seconds":
                        float(
                            selected_prediction_row[
                                "time_seconds"
                            ]
                        ),

                    "signed_time_error_seconds":
                        time_error,

                    "absolute_time_error_seconds":
                        abs(time_error),

                    "matched":
                        1,

                }

            )


        else:

            output_rows.append(

                {

                    "ground_truth_transition_id":
                        ground_truth_index + 1,

                    "from_state":
                        ground_truth_row["from_state"],

                    "to_state":
                        ground_truth_row["to_state"],

                    "ground_truth_frame":
                        int(
                            ground_truth_row[
                                "frame_number"
                            ]
                        ),

                    "predicted_frame":
                        np.nan,

                    "signed_frame_error":
                        np.nan,

                    "absolute_frame_error":
                        np.nan,

                    "ground_truth_time_seconds":
                        float(
                            ground_truth_row[
                                "time_seconds"
                            ]
                        ),

                    "predicted_time_seconds":
                        np.nan,

                    "signed_time_error_seconds":
                        np.nan,

                    "absolute_time_error_seconds":
                        np.nan,

                    "matched":
                        0,

                }

            )


    return pd.DataFrame(output_rows)


